package com.epam.info_handling.constant;

/**
 * Class stores byte operation constants.
 */
public final class ByteOperation {
    /**
     * Operation OR.
     */
    public static final String OR = "|";
    /**
     * Operation AND.
     */
    public static final String AND = "&";
    /**
     * Operation XOR.
     */
    public static final String XOR = "^";
    /**
     * Operation NOT.
     */
    public static final String NOT = "~";
    /**
     * Operation RIGHT SHIFT.
     */
    public static final String RIGHT_SHIFT = ">>";
    /**
     * Operation LEFT SHIFT.
     */
    public static final String LEFT_SHIFT = "<<";
    /**
     * Operation RIGHT SHIFT with fill nulls.
     */
    public static final String RIGHT_SHIFT_FILL_NULL = ">>>";

    /**
     * Private constructor.
     */
    private ByteOperation() {
    }
}
